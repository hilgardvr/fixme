    public void displayHoldings(Map<String, Integer> newMap) {
        HoldingsComboBox.removeAllItems();
        BuyPriceTextField.setText("");
        BuyVolumeTextField.setText("");
        BuyShareTextField.setText("");
        SellPriceTextField.setText("");
        SellVolumeTextField.setText("");
        for (Map.Entry<String, Integer> hold : newMap.entrySet()) {
            String stock = hold.getKey();
            Integer volume = hold.getValue();
            HoldingsComboBox.addItem(stock + " amount held: " + volume);// + "/t" + volume.toString());
        }
    }

