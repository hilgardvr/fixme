    private String formatDecimals(double d) {
        DecimalFormat df = new DecimalFormat("#0.00");
        return df.format(d);
    }

    public void displayCashBalance(Double bal) {
        CashBalanceTextField.setText(formatDecimals(bal));
    }

    public void setHoldingsValue(double d) {
        HoldingsValueTextField.setText(formatDecimals(d));
    }

    public void setTotalAccountValue(double d) {
        TotalAccountValueTextField.setText(formatDecimals(d));
    }

